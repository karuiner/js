// 하노이의 탑
function solution(n) {
  function f(n, s, m, e) {
    let ans = [];
    if (n === 1) {
      ans.push([s, e]);
    } else {
      ans = [...f(n - 1, s, e, m), [s, e], ...f(n - 1, m, s, e)];
    }
    return ans;
  }

  return f(n, 1, 2, 3);
}
// n= 1  [1,3] s=1 m=2 e=3
// n= 2  [1,2] => [1,3] => [2,3]
// n= 3  [1,3] => [1,2] => [3,2] => [1,3] => [2,1] => [2,3] => [1,3]
