var splitPainting = function (segments) {
  function pick(arr, k) {
    let a = 0,
      b = arr.length - 1;
    while (a < b) {
      let m = Math.floor(0.5 * (a + b));
      if (arr[m][1] < k) {
        a = m + 1;
      } else if (arr[m][0] > k) {
        b = m;
      } else {
        a = m;
        b = m;
      }
    }
    return a;
  }

  function f(ans, arr) {
    let nans = [];
    if (ans.length === 0) {
      nans.push(arr);
    } else if (arr[1] <= ans[0][0]) {
      nans = nans.concat([arr, ...ans]);
    } else if (arr[0] >= ans[ans.length - 1][1]) {
      nans = nans.concat([...ans, arr]);
    } else {
      let k = pick(ans, arr[0]),
        z = ans.length - 1;
      for (let i = k; i < ans.length; i++) {
        let [a, b, c] = ans[i],
          [s, e, v] = arr;
        if (s >= b || a >= e || arr.length === 0) {
          nans.push([a, b, c]);
        } else {
          let p = a;
          if (a !== s) {
            if (a < s) {
              nans.push([a, s, c]);
              p = s;
            } else {
              nans.push([s, a, v]);
            }
          }
          if (b < e) {
            nans.push([p, b, c + v]);
            arr = [b, e, v];
          } else {
            nans.push([p, e, c + v]);
            if (e !== b) {
              nans.push([e, b, c]);
            }
            z = i;
            arr = [];
            break;
          }
        }
      }

      if (arr.length > 0) {
        nans.push(arr);
      }
      nans = [...ans.slice(0, k), ...nans, ...ans.slice(z + 1)];
    }
    nans.sort((a, b) => a[0] - b[0]);
    return nans;
  }

  let ans = [];
  for (let i of segments) {
    ans = f(ans, i);
    //        console.log(i, ans)
  }
  return ans;
};

